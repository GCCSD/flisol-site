<h1>Saiba mais sobre o Flisol</h1>

<p>
    O <a href="<?php echo URL; ?>organizadores" title="Site do GCCSD">GCCSD</a> traz para Santos,
    pelo oitavo ano, o <strong>Festival Latino Americano de Instalação de Software Livre</strong>
    (<strong>Flisol</strong>). O Flisol é um evento regional, realizado anualmente, e que ocorre
    de forma simultânea em diversas cidades da América Latina. O Flisol é um evento descentralizado,
    onde cada local escolhido realiza seu festival simultaneamente com os outros pontos espalhados
    pela América Latina.
</p>

<p>
    O evento é livre, nesse dia há a instalação de <strong>Software Livre</strong>, como distribuições
    de <strong>Gnu/Linux</strong>, <strong>sistemas BSD</strong>, e aplicativos livres para Windows em
    geral, além de palestras e oficinas.
</p>

<h2>Objetivo do evento</h2>

<p>
    O objetivo do Flisol é promover o uso de software livre em geral, além de criar interações entre
    usuários e desenvolvedores.
</p>

<h2>Mais sobre o evento</h2>

<p>O <strong>FLISOL</strong> acontece simultaneamente em diversos países da América Latina, inclusive no Brasil. Santos receberá sua sexta edição neste ano. Para esse fim as diversas comunidades locais de Software Livre em cada país, cidade ou localidade, organizam simultaneamente eventos em que se instala, de maneira gratuita e totalmente legal, softwares livres nos computadores dos participantes que se interessarem. Também são oferecidos mini cursos e palestras em torno de temas que envolvem o Software Livre em todas suas expressões: artística, acadêmica, empresarial e social.</p>

<p>O <strong>FLISOL</strong> acontecerá no <strong>sábado dia 26 de abril de 2014</strong>.<br>
Local: SESC Santos - Rua Conselheiro Ribas, 136 (próximo ao canal 5)<br>
Veja o <a href="<?php echo URL; ?>data_hora">mapa de localização.</a></p>

<h2>Quem organiza o FLISOL ?</h2>

<p>As comunidades de Software Livre na América Latina, formadas por pessoas e grupos de diversas áreas, com o apoio de outras entidades, principalmente educacionais e patrocinadores. Em Santos o Grupo do Compartilhamento do Conhecimento Santos Dumont (GCC-SD) é o responsável pela organização.</p>

<h2>A quem o evento é dirigido ?</h2>

<p>O evento é dirigido a todo tipo de público: estudantes, empresários, entusiastas e pessoas que não têm muito conhecimento sobre softwares.</p>

<p>Aqueles que não possuem conhecimentos sobre informática, licenças de software, copyright e demais áreas da ciência e da tecnologia também podem assistir e participar do evento.</p>

<p>Nosso objetivo é compartilhar conhecimento, buscando a liberdade.</p>

<h2>Quanto custa a participação no evento ?</h2>

<p>A participação no evento é totalmente livre e gratuita.</p>

<h2>Que benefícios têm os participantes do FLISOL?</h2>

<p>Entre alguns outros benefícios os participantes terão a oportunidade de instalar Softwares Livres em seus computadores, perceber que é uma alternativa real e segura a outros modelos de desenvolvimento e distribuição de software. Informar-se sobre sua filosofia, cultura e organização ao redor do mundo, conhecer as experiências, desenvolvimentos e estudos de grupos e entidades relacionadas ao Software Livre no âmbito local, nacional e internacional.</p>

<p>Existe também a possibilidade de encontrar uma alternativa ou solução que use Software Livre para sua empresa, colégio, universidade ou para seu governo. </p>

<p>O participante poderá esclarecer suas dúvidas sobre os temas relacionados e, quem sabe, encontrar no Software Livre uma alternativa de estudos, emprego ou desenvolvimento tecnológico.</p>

<h2>Como sua entidade pode apoiar essa iniciativa</h2>

<ul>
    <li>Oferecendo o espaço físico para realização do evento.</li>
    <li>Oferecendo estrutura de rede e acesso a Internet. </li>
    <li>Permitindo a colocação de cartazes e distribuição de folhetos promocionais.</li>
    <li>Permitindo a visita da organização a salas de aula divulgando o evento.</li>
    <li>Oferecendo espaço para realização de palestras promocionais sobre Software Livre antes do evento.</li>
    <li>Envolver o FLISOL nas atividades acadêmicas dos cursos relacionados a informática.</li>
</ul>

<h2>Como sua entidade pode patrocinar essa iniciativa</h2>

<p>Arcando com parte dos custos, que envolvem:</p>

<ul>
    <li>Confecção de material de divulgação.</li>
    <li>Confecção de brindes para serem distribuídos no evento.</li>
    <li>Operacionalização do evento, translado de palestrantes e operadores.</li>
    <li>Gravação de Cds para distribuição entre os participantes.</li>
    <li>Doação de livros, acessórios de informática para sorteio no evento.</li>
</ul>

