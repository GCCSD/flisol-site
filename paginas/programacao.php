<h1>Programação para o Flisol 2012</h1>

<h2>Instalações e tira dúvidas durante o dia todo</h2>
<p>
    Durante todo o evento serão feitas instalações e tira dúvidas sobre softwares livres com a equipe do Flisol.
</p>

<h2>9h - Abertura</h2>
<p>Espaço para bate papo.</p>
<h2>9h30 às 10h30 - Palestra: Software Livre (Paulo Kretcheu)</h2>

<p>História do movimento do Software Livre, o ponto de visto social e tecnológico do seu uso, quais são as diferenças em relação aos Softwares Proprietários, a colaboração entre os usuários, a liberdade do conhecimento, entre outros assuntos.</p>

<h2>11h às 13h - Mesa redonda sobre Cultura Digital (Organização Flisol)</h2>

<p>Discussão sobre protocolos únicos, a escolha do software livre como forma de liberdade e autonomia de usuários, webdemocracia, direitos autorais, ciberativismo, a Internet sob ataque.</p>

<h2>14h às 16h - Ocupa FLISOL (livre)</h2>

<p>Espaço disponível para os participantes ocuparem da forma como desejarem, seja em debates com tema livre, seja com performances, seja propondo discussões sobre cultura digital, etc.</p>

<h2>16h às 17h - Hardware Livre (Álvaro Justen – Turicas)</h2>

<p>Projetos com o controlador lógico programável Arduino. Apresentação do hardware livre, para que serve, de que forma se trabalha com ele, etc.</p>
