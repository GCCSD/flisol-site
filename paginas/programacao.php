<h1>Programação</h1>
<h2>Install Fest (9h às 17h)</h2>
<p>Durante todo o dia acontecerão instalações de softwares livres nos computadores dos participantes interessados.</p>
<p>A equipe organizadora do Flisol Santos conta com técnicos especializados em Software Livre. Esses profissionais podem ajudar os participantes a:</p>
<ul>
    <li>Instalar Softwares Livres em seus computares. Sejam distribuições GNU/Linux ou softwares livres para a plataforma Windows(r).</li>
    <li>Resolver problemas pós instalação de distribuições GNU/Linux.</li>
    <li>Tirar dúvidas em geral sobre o uso de Softwares Livres.</li>
</ul>
<h2>Área de Jogos (9h às 17h)</h2>
<p>Durante o evento haverá uma área dedicada aos jogos em ambientes GNU/Linux. Esta área contará com a presença do Carlos Donizete (Coringão), criador e mantenedor do site <a href="http://www.ubuntugames.org/" title="Site do Ubuntu Games">Ubuntu Games</a>.</p>
