<h1>Saiba mais sobre o Flisol</h1>

<h2>A Internet está sob ataque?</h2>

<p>O advento de projetos de leis como o ACTA, SOPA e PIPA, o fechamento do site Megaupload e a reforma do direito autoral são assuntos que colocam a liberdade do usuário da grande rede mundial de computadores em cheque. </p>

<p>A crise na Internet que recentemente foi intitulada como a “terceira Guerra Mundial” por internautas está sendo discutida nos mais diversos meios e ganha um espaço cada vez maior na grande mídia. É sob a necessidade de se discutir a liberdade e a diversidade cultural na rede que anunciamos o "Festival Latino Americano de Instalação de Software Livre", FLISOL, em Santos.</p>

<p>O <strong>FLISOL</strong> é o maior evento de difusão de Software Livre na América Latina. Vem sendo realizado desde 2005 e tem como objetivo principal, além de promover o uso do Software Livre, esclarecer sua filosofia, avanços, viabilidade técnica, social e econômica. O evento é destinado a estudantes, empresários e o público em geral.
</p>
<p>Durante o <strong>FLISOL</strong> serão discutidos o uso do Software Livre como forma de emancipação dos usuários na rede mundial de computadores, além da necessidade de se colocar os usuários em posição de equilíbrio com os desenvolvedores e empresas produtoras de software. “Esse equilíbrio interfere nos sistemas de desenvolvimento e de acesso aos conhecimentos, tornando todos partícipes do processo”, afirma Paulo Kretcheu, professor universitário e consultor de segurança e redes.</p>

<p>Segundo <strong>Kretcheu</strong>, o usuário de softwares livres, ainda que sem perceber, fortalece o modelo de desenvolvimento colaborativo e acaba por se beneficiar mesmo que não envolvido diretamente. O usuário de software proprietário, em boa parte usa de modo ilegal, cometendo um crime e ficando cada vez mais dependente do modelo que lhe desfavorece.</p>

<p>O evento se propõe a debater a produção de conteúdo de forma colaborativa na Internet, quais são os limites da liberdade de criação e inovação, leis de direitos autorais, as diferenças entre os softwares livres e proprietários, entre outros assuntos em pauta.</p>

<h2>Mais sobre o evento</h2>

<p>O <strong>FLISOL</strong> acontece simultaneamente em diversos países da América Latina, inclusive no Brasil. Santos receberá sua sexta edição neste ano. Para esse fim as diversas comunidades locais de Software Livre em cada país, cidade ou localidade, organizam simultaneamente eventos em que se instala, de maneira gratuita e totalmente legal, softwares livres nos computadores dos participantes que se interessarem. Também são oferecidos mini cursos e palestras em torno de temas que envolvem o Software Livre em todas suas expressões: artística, acadêmica, empresarial e social.</p>

<p>O <strong>FLISOL</strong> acontecerá entre <strong>quinta, sexta e sábado entre os dias 26 e 28 de abril de 2012, em Santos</strong>.</p>

